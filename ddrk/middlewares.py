class DownloadMiddlewares:

    def process_request(self, request, spider):
        """
        使用playwright模拟点击获取video视频链接
        """
        proxies = 'http://172.17.0.2:7890'
        with sync_playwright() as playwright:
            browser = playwright.chromium.launch(proxy={'server': proxies})
            context = browser.new_context()
            page = context.new_page()
            page.goto(request.url)
            page.wait_for_selector('button.vjs-big-play-button', timeout=10000)
            page.click('button.vjs-big-play-button')
            page.wait_for_selector('div#vjsp>video[src^="https"]', timeout=10000)
            content = page.content()

            browser.close()
            return HtmlResponse(url=request.url, status=200, body=content, encoding='utf-8',
                                request=request)

